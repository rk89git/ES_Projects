package com.db.wisdom.model;

public class Competitor {

	public String name;
	public Long fans;
	public Long posts;
	public Long engagement;
	public Long avgEngagement;
	public Long shares;
	public Long avgShares;
	public Long videoViews;
	public Long avgVideoViews;
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public Long getFans() {
		return fans;
	}
	public void setFans(Long fans) {
		this.fans = fans;
	}
	public Long getPosts() {
		return posts;
	}
	public void setPosts(Long posts) {
		this.posts = posts;
	}
	public Long getEngagement() {
		return engagement;
	}
	public void setEngagement(Long engagement) {
		this.engagement = engagement;
	}
	public Long getAvgEngagement() {
		return avgEngagement;
	}
	public void setAvgEngagement(Long avgEngagement) {
		this.avgEngagement = avgEngagement;
	}
	public Long getShares() {
		return shares;
	}
	public void setShares(Long shares) {
		this.shares = shares;
	}
	public Long getAvgShares() {
		return avgShares;
	}
	public void setAvgShares(Long avgShares) {
		this.avgShares = avgShares;
	}
	public Long getVideoViews() {
		return videoViews;
	}
	public void setVideoViews(Long videoViews) {
		this.videoViews = videoViews;
	}
	public Long getAvgVideoViews() {
		return avgVideoViews;
	}
	public void setAvgVideoViews(Long avgVideoViews) {
		this.avgVideoViews = avgVideoViews;
	}
	
	
	
}
