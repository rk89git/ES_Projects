package com.db.common.model;

public class Hours {
	
	private int hour;
	private String unique_story_count;
	private String number_of_slides;
	
	public int getHour() {
		return hour;
	}
	public void setHour(int hour) {
		this.hour = hour;
	}
	public String getUnique_story_count() {
		return unique_story_count;
	}
	public void setUnique_story_count(String unique_story_count) {
		this.unique_story_count = unique_story_count;
	}
	public String getNumber_of_slides() {
		return number_of_slides;
	}
	public void setNumber_of_slides(String number_of_slides) {
		this.number_of_slides = number_of_slides;
	}
	
	

}
